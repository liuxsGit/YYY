package vo;

public class repairTable {
	private String repairID;
	private String repairType;
	private String repairItem;
	private String roomID;
	private String repair_userID;
	private String repairDate;
	private String state;

	public String getRepairType() {
		return repairType;
	}
	
	public void setRepairType(String repairType) {
		this.repairType = repairType;
	}

	public String getRepairItem() {
		return repairItem;
	}
	
	public void setRepairItem(String repairItem) {
		this.repairItem = repairItem;
	}
	
	public String getRoomID() {
		return roomID;
	}
	
	public void setRoomID(String roomID) {
		this.roomID = roomID;
	}
	
	public String getRepair_userID() {
		return repair_userID;
	}
	
	public void setRepair_userID(String repair_userID) {
		this.repair_userID = repair_userID;
	}
	
	public String getRepairDate() {
		return repairDate;
	}
	
	public void setRepairDate(String repairDate) {
		this.repairDate = repairDate;
	}
	
	public String getState() {
		return state;
	}
	
	public void setState(String state) {
		this.state = state;
	}
	
	public String getRepairID() {
		return repairID;
	}
	
	public void setRepairID(String repairID) {
		this.repairID = repairID;
	}
}
